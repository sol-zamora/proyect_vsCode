import math #modulo que contiene codigo en python

print(round( 1.3)) #Lo que hace es redondear hacia abajo, ya que esta mas cerca del numero uno en este caso
print(round(1.7)) #En este caso redondea hacia arriba en este caso hacia 2
print(round(1.5)) #En este caso tambien redondea hacia arriba, porque esta a la mitad del numero
print(abs(-77)) #Devuelve el numero absoluto es decir si esta en negativo lo pasa a positivo y nos devuelve el mismo valor
print(abs(55)) #en este caso como es positivo se devulve el mismo numero


print(math.ceil(1.1)) # esta funcion toma el numero y lo lleva al numero mas cercano hacia arriba en este caso es 2
print(math.floor(1.999999)) # floor toma el numero y lo lleva al numero mas cercano pero de abajo en este caso 1
print(math.isnan(25)) # isnan nos indica si el argumento que le estamos pasando es un numero 
